window.currentLang = 'en';

async function detectRegion() {
    try {
        // Using a free geo-IP service for demonstration
        const response = await fetch('https://ipapi.co/json/');
        const data = await response.json();

        if (data.region_code === 'MH' || data.region === 'Maharashtra') {
            setLanguage('mr');
        }
    } catch (error) {
        console.log("Region detection failed, defaulting to English");
    }
}

function setLanguage(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    document.getElementById('langToggle').textContent = lang.toUpperCase();

    // Update text content elements marked with data-t
    document.querySelectorAll('[data-t]').forEach(el => {
        const key = el.getAttribute('data-t');
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // Update placeholder elements marked with data-t-placeholder
    document.querySelectorAll('[data-t-placeholder]').forEach(el => {
        const key = el.getAttribute('data-t-placeholder');
        if (translations[lang][key]) {
            el.setAttribute('placeholder', translations[lang][key]);
        }
    });

    // Update page title
    if (translations[lang].page_title) {
        document.title = translations[lang].page_title;
    }

    // Trigger calendar re-render if it exists
    if (window.renderMiniCalendar) {
        window.renderMiniCalendar();
    }
    if (window.renderUpcoming) {
        window.renderUpcoming();
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const toggleBtn = document.getElementById('langToggle');
    if (toggleBtn) {
        toggleBtn.addEventListener('click', () => {
            setLanguage(window.currentLang === 'en' ? 'mr' : 'en');
        });
    }

    detectRegion();
});
